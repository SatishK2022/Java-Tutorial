public class literal
{
    public static void main(String[] args)
    {
        // Literals --> A constant value which can be assigned to a variable is called as literal.
        // Keywords --> Keywords are the reserved words which is used by the java compiler and cannot be used as an identifiers.

        // Integrals
        byte age = 19;
        int age1 = 30;
        short age2 = 98;
        long accountBalance = 7574757748l;   // Here l in mandatory

        // Floats
        float f1 = 5.2f;  // Here f/F is mandatory
        double d1 = 3.2d; // Here d/D is optional

        // Char
        char ch = 'A';

        // Boolean
        boolean name = true;

        // String
        String str = "Satish";

        System.out.println(str);
        System.out.println(age);
        System.out.println(name);
        System.out.println(age1);
        System.out.println(age2);
        System.out.println(accountBalance);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);

    }
}